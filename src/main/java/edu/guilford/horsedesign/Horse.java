
package edu.guilford.horsedesign;


public class Horse {
    private String hMane;
    private String hTail;
    private String hBody;
    private String hHooves;

    public String gethMane() {
        return hMane;
    }

    public void sethMane(String hMane) {
        this.hMane = hMane;
    }

    public String gethTail() {
        return hTail;
    }

    public void sethTail(String hTail) {
        this.hTail = hTail;
    }

    public String gethBody() {
        return hBody;
    }

    public void sethBody(String hBody) {
        this.hBody = hBody;
    }

    public String gethHooves() {
        return hHooves;
    }

    public void sethHooves(String hHooves) {
        this.hHooves = hHooves;
    }
    
    
    
    
}
